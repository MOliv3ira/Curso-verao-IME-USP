#include <stdio.h>
#include <stdlib.h>


void main(){

    int a = 1;

    //CONTANDO ATÉ 10

    while(a <= 10){

        printf("\n%d", a);

        //INCREMENTO

        a = a + 1;
        // a++;
    }

    //CONTAGEM REGRESSIVA

    int b = 10;

    while(b >= 1){

        printf("\n%d", b);

        //INCREMENTO

        b = b - 1;
        // b--;
    }


system("pause");
}
